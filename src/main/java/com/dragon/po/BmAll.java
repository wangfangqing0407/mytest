public class BmAll {

    private String bh;
    private String mc;
    private String fl;
    private String mk;
    private String bz;
    private String xh;

    public BmAll() {
    }

    public BmAll(String bh, String mc, String fl, String mk, String bz, String xh) {
        this.bh = bh;
        this.mc = mc;
        this.fl = fl;
        this.mk = mk;
        this.bz = bz;
        this.xh = xh;
    }

    public String getBh() {
        return bh;
    }

    public void setBh(String bh) {
        this.bh = bh;
    }

    public String getMc() {
        return mc;
    }

    public void setMc(String mc) {
        this.mc = mc;
    }

    public String getFl() {
        return fl;
    }

    public void setFl(String fl) {
        this.fl = fl;
    }

    public String getMk() {
        return mk;
    }

    public void setMk(String mk) {
        this.mk = mk;
    }

    public String getBz() {
        return bz;
    }

    public void setBz(String bz) {
        this.bz = bz;
    }

    public String getXh() {
        return xh;
    }

    public void setXh(String xh) {
        this.xh = xh;
    }
}
